										 Confeccionar el protocolo de procedimiento para la Revisión Técnica
										Obligatoria (RTO) y la Revisión Rápida y Aleatoria (RRA).<br>
										 Establecer los valores mínimos/máximos admisibles para evaluar cada
										aspecto que involucre la Revisión Técnica Obligatoria (RTO).<br>
