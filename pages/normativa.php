										(TRT), la Autoridad Jurisdiccional deberá disponer el cierre del taller.<br>
										18.- La Revisión Técnica Obligatoria (RTO) será efectuada por talleres
										habilitados al efecto, los cuales funcionarán bajo la "Dirección Técnica" de un
