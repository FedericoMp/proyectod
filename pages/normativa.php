										Revisión Técnica (TRT).<br>
										 Llevar el registro de profesionales a que se refiere el apartado 18.-.<br>
										15.- Cada Autoridad Jurisdiccional dispondrá, de acuerdo a sus prioridades, las
										acciones necesarias para poner escalonadamente en funcionamiento el Sistema
										de Revisión Técnica Obligatoria (RTO). El Ente Auditor Nacional no tendrá
										competencia sobre los sistemas de Revisión Técnica Obligatoria que
										implementen la jurisdicción nacional o la local para los vehículos que no sean de
										estricto uso particular.<br>
										16.- Cada Autoridad Jurisdiccional determinará el número de talleres revisores
										que funcionarán en su jurisdicción, garantizando los procedimientos a los que 

										se sujetará la selección y habilitación de los mismos.<br>
										17.- Cada Autoridad Jurisdiccional deberá establecer un régimen de sanciones a
										aplicar a todos los talleres que funcionen bajo su jurisdicción. El mismo podrá
										contemplar sanciones económicas, pero obligatoriamente deberá establecer las
										condiciones para aplicar sanciones de apercibimiento, suspensión temporaria y
