										ser mayor a DOCE (12) meses.<br>
										4.- Los vehículos detectados en inobservancia a lo establecido en los items 1.-,
										2.- ó 3.-, podrán ser emplazados en forma perentoria por la Autoridad
										Jurisdiccional (AJ) a efectuar la Revisión Técnica Obligatoria (RTO), sin perjuicio
